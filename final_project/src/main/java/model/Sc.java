package model;

public class Sc {
	private String sc;
	private String lc;
	
	public Sc(String sc, String lc) {
		super();
		this.sc = sc;
		this.lc = lc;
	}
	public String getSc() {
		return sc;
	}
	public void setSc(String sc) {
		this.sc = sc;
	}
	public String getLc() {
		return lc;
	}
	public void setLc(String lc) {
		this.lc = lc;
	}
	
	
}
